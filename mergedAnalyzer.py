    def __init__(self):
        self.table = {}  # Transition Table 
        self.currentState = "T0"  #  state T0 
        self.finalStates = {}  # Final State 

    def SetTransitionTable(self, dfa):  # Transition Table 
        self.table = dfa["Table"]
        self.finalStates.update(dfa["FinalStates"])

    def CheckNextState(self, _input):  #  state check
        if not _input in self.table[self.currentState]:  #     input      
            print("error")
            exit()
        nextState = self.table[self.currentState][_input]
        if nextState == "":  #  state rejected  "rejected" 
            return "rejected"
        else:  #   nextState 
            return nextState

    def GetState(self):  #  state 
        return self.currentState

    def SetState(self, _state):
        self.currentState = _state

    def GetToken(self):  #   state   ex)INTEGER, STRING ---
        if self.currentState in self.finalStates:
            return self.finalStates[self.currentState]
        else:  #    state final state  error 
            return "Error"

    def isFinalState(self):  #  state final state check
        if self.currentState in self.finalStates:
            return True
        else:
            return False

    def Reset(self):  # dfa reset
        self.currentState = "T0"


